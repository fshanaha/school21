#include "libft.h"

int	ft_power(int x, int n)
{
	int a;

	a = 1;
	while (n)
	{
		if (n % 2)
		{
			a *= x;
			n--;
		}
		else
		{
			x *= x;
			n /= 2;
		}
	}
	return (a);
}
