#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int a;

	a = 0;
	while (s[a] != (char)c)
	{
		if (s[a] == '\0')
			return (0);
		a++;
	}
	return ((char*)&s[a]);
}
